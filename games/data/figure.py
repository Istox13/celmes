def coords_init(figure, key, x=5, y=0):
    t_s = 0
    coords = list()

    for i, row in enumerate(figure[key]):
        if sum(row) == 0:
            t_s += 1
            continue

        for j, n in enumerate(row):
            if n == 1:
                coords.append((x + j, y + i - t_s))

    return coords, (figure, key)


